#calculator using functions
def addition(a,b):
    sum=a+b
    return(sum)
def subtraction(a,b):
    sub=a-b
    return(sub)
def multiplication(a,b):
    mul=a*b
    return(mul)
def division(a,b):
    div=a*b
    return(div)
print(addition(2,3))
print(subtraction(2,3))
print(multiplication(2,3))
print(division(2,3))


